package com.android.easycake.models;

public class MODELProduct {

	String _productId, _storeId, _categoryId, _productName, _productWeight, _ingredient, _price;

	public String get_productId() {
		return _productId;
	}

	public void set_productId(String productId) {
		_productId = productId;
	}

	public String get_storeId() {
		return _storeId;
	}

	public void set_storeId(String storeId) {
		_storeId = storeId;
	}

	public String get_categoryId() {
		return _categoryId;
	}

	public void set_categoryId(String categoryId) {
		_categoryId = categoryId;
	}

	public String get_productName() {
		return _productName;
	}

	public void set_productName(String productName) {
		_productName = productName;
	}

	public String get_productWeight() {
		return _productWeight;
	}

	public void set_productWeight(String productWeight) {
		_productWeight = productWeight;
	}

	public String get_ingredient() {
		return _ingredient;
	}

	public void set_ingredient(String ingredient) {
		_ingredient = ingredient;
	}

	public String get_price() {
		return _price;
	}

	public void set_price(String price) {
		_price = price;
	}



}
